# Variables, data types, assignment, and basic operations in Python

# Variable declarations
name = "Nihal Mishra"
age = 20
price = 19.00
is_active = True
data = None

# Display variables
print("Name:", name)
print("Age:", age)
print("Price:", price)

# Assignment operator
age2 = age
print("Copied Age:", age2)

# Data type inspection
print(type(name))       # str
print(type(age))        # int
print(type(price))     # float
print(type(is_active)) # bool
print(type(data))      # NoneType

# Simple arithmetic using variables
num1 = 10
num2 = 20
total = num1 + num2
print("The sum of", num1, "and", num2, "is", total)

# Numeric representations
print(30)      # Integer
print(0.577)   # Float
